
public class ProcessCasting {

	public static void main(String[] args) {
		
			double b, B, h, area;
			int x1, y1;
			
			double result;
			
			x1 = 5;
			y1 = 2;
			
			b = 6.0;
			B = 8.0;
			h = 5.0;
			
			area = (b + B) / 2.0 * h;
			System.out.println(area);
			
		
			
			result = (double) x1 / y1;
			System.out.println(result);
	}

}
